package com.sp.travel;

public class WorkLand {
	private int landCode;
	private String landName;
	private String saveFileName;
	private int workLandCode;
	private String tagName;
	private String orderNum;
	private String lat;
	private String lng;
	private int budget;
	private String memo;
	
	
	
	
	public int getBudget() {
		return budget;
	}
	public void setBudget(int budget) {
		this.budget = budget;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}
	public int getLandCode() {
		return landCode;
	}
	public void setLandCode(int landCode) {
		this.landCode = landCode;
	}
	public String getLandName() {
		return landName;
	}
	public void setLandName(String landName) {
		this.landName = landName;
	}
	public String getSaveFileName() {
		return saveFileName;
	}
	public void setSaveFileName(String saveFileName) {
		this.saveFileName = saveFileName;
	}
	public int getWorkLandCode() {
		return workLandCode;
	}
	public void setWorkLandCode(int workLandCode) {
		this.workLandCode = workLandCode;
	}
	public String getTagName() {
		return tagName;
	}
	public void setTagName(String tagName) {
		this.tagName = tagName;
	}
	
	
	
}
